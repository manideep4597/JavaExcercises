public class TomJerry {


    public static String main(int user_i)
    {


        if(user_i <30 &&user_i>20&&user_i%2==1)
        {
            return " TOM " ;
        }
        else if(user_i <30 &&user_i>20&&user_i%2==0)
        {
            return " JERRY " ;
        }

        else
        {
            return "Not in range" ;
        }

    }
}
